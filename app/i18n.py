def normalize_lang(code: str | None) -> str:
    if code in LANGUAGES:
        return code
    return config.DEFAULT_LANG if config.DEFAULT_LANG in LANGUAGES else "en"


def translate(lang: str, key: str) -> str:
    lang = normalize_lang(lang)
    table = TRANSLATIONS.get(lang, {})
    return table.get(key) or TRANSLATIONS["en"].get(key, key)


def level_labels(lang: str) -> dict[str, str]:
    return {level: translate(lang, f"level.{level}") for level in config.LEVELS}
